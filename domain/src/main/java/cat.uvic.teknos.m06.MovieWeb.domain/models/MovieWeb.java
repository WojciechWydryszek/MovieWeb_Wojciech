package cat.uvic.teknos.m06.MovieWeb.domain.models;

public class MovieWeb {
    private String films, actor, main_character, gender;

    public String getFilms() { return films; }

    public void setFilms(String films) { this.films = films; }

    public String getActor() { return actor; }

    public void setActor(String actor) { this.actor = actor; }

    public String getMain_character() { return main_character; }

    public void setMain_character(String main_character) { this.main_character = main_character; }

    public String getGender() { return gender; }

    public void setGender(String gender) { this.gender = gender; }
}